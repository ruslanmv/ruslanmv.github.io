// Generates the *local* MCP Inspector command for stdio / local servers.
// The website never executes this — it only builds copy/paste text. Local
// stdio MCP servers must be inspected from the user's own machine.

const DEFAULTS = {
  serverUrl: 'https://your-mcp-server.example.com/mcp',
  token: '',
  timeout: '120',
  pythonPath: '/path/to/venv/bin/python',
  wrapperModule: 'mcpgateway.wrapper'
};

function shellQuote(value) {
  // Double-quote and escape characters that are special inside double quotes.
  return `"${String(value).replace(/(["$`\\])/g, '\\$1')}"`;
}

export function buildInspectorCommand(opts = {}) {
  const o = { ...DEFAULTS, ...clean(opts) };
  const authValue = o.token ? `Bearer ${o.token}` : 'Bearer YOUR_TOKEN';

  const lines = [
    'npx @modelcontextprotocol/inspector \\',
    `  -e MCP_AUTH=${shellQuote(authValue)} \\`,
    `  -e MCP_SERVER_URL=${shellQuote(o.serverUrl)} \\`,
    `  -e MCP_TOOL_CALL_TIMEOUT=${shellQuote(o.timeout)} \\`,
    `  ${o.pythonPath} -m ${o.wrapperModule}`
  ];
  return lines.join('\n');
}

export function buildShellScript(opts = {}) {
  const o = { ...DEFAULTS, ...clean(opts) };
  const command = buildInspectorCommand(o);
  return [
    '#!/usr/bin/env bash',
    '# Generated by the RuslanMV MCP Server Inspector (https://ruslanmv.com/assets/tools/mcp-inspector.html)',
    '# Run this on your own machine to inspect a local/stdio MCP server.',
    '# Tip: prefer exporting the token from your shell rather than committing it.',
    'set -euo pipefail',
    '',
    o.token
      ? '# NOTE: a token is embedded below — do not commit this file to version control.'
      : '# export MCP_TOKEN="paste-your-token-here"   # then replace YOUR_TOKEN below',
    '',
    command,
    ''
  ].join('\n');
}

function clean(opts) {
  const out = {};
  for (const [key, value] of Object.entries(opts)) {
    if (value !== undefined && value !== null && String(value).trim() !== '') {
      out[key] = String(value).trim();
    }
  }
  return out;
}
