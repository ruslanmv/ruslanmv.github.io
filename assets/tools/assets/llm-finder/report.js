// Page 2 controller — render the selected model's report.
import { $, showToast, copyText } from '../shared/tool-shell.js';
import { downloadFile, timestampSlug } from '../shared/export.js';
import { loadSelectedModel, loadQuery } from './storage.js';
import { renderReport, buildSummaryText } from './render-report.js';

const root = $('#reportRoot');
const model = loadSelectedModel();
const query = loadQuery();

function emptyState() {
  root.innerHTML = `
    <div class="rep-empty">
      <div class="rep-empty-icon">🔍</div>
      <h2>No model selected</h2>
      <p>Go back to the finder and choose a model to see its compatibility report.</p>
      <a class="btn primary" href="llm-compatibility-finder.html">Open the finder</a>
    </div>`;
}

function buildMarkdown(m, q) {
  const c = m.commands || {};
  return `# LLM Compatibility Report — ${m.name || m.repoId}

- **Repository:** ${m.repoId}
- **Compatibility:** ${m.compatibilityScore}%
- **Best target:** ${m.recommendedTarget || (m.compatibleApps || [])[0] || '—'}
- **Recommended quant:** ${m.recommendedQuant || '—'}
- **Estimated memory:** ${m.estimatedMemoryGb != null ? '~' + m.estimatedMemoryGb + ' GB' : '—'}
- **License:** ${m.license || '—'}
${q && q.profileLabel ? `- **Hardware profile:** ${q.profileLabel}` : ''}

## Commands
${c.ollama ? '```sh\n' + c.ollama + '\n```\n' : ''}${c.llamaCppServer ? '```sh\n' + c.llamaCppServer + '\n```\n' : ''}${c.llamaCppCli ? '```sh\n' + c.llamaCppCli + '\n```\n' : ''}${c.vllm ? '```sh\n' + c.vllm + '\n```\n' : ''}
## Why compatible
${(m.why || []).map((w) => '- ' + w).join('\n') || '- (n/a)'}

## Warnings
${(m.warnings || []).map((w) => '- ' + w).join('\n') || '- Compatibility is estimated, not guaranteed.'}

_Generated by the LLM Compatibility Finder. Compatibility is estimated and may vary._
`;
}

function wire() {
  root.addEventListener('click', (e) => {
    const c = e.target.closest('[data-copy-command]');
    if (c) {
      const key = c.dataset.copyCommand;
      const cmd = (model.commands || {})[key];
      if (cmd) copyText(cmd, 'Command copied'); else showToast('No command available');
      return;
    }
    if (e.target.closest('#copySummary')) {
      copyText(buildSummaryText(model, query), 'Summary copied');
      return;
    }
    if (e.target.closest('#downloadReport')) {
      const slug = (model.repoId || 'model').replace(/[^a-z0-9]+/gi, '-').toLowerCase();
      downloadFile(`llm-report-${slug}-${timestampSlug()}.md`, buildMarkdown(model, query), 'text/markdown;charset=utf-8');
    }
  });
}

if (!model) {
  emptyState();
} else {
  root.innerHTML = renderReport(model, query);
  wire();
}
