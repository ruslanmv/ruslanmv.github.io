export default {
  async fetch(request, env) {
    const origin = request.headers.get("Origin") || "";

    if (request.method === "OPTIONS") {
      return new Response(null, { status: 204, headers: corsHeaders(origin) });
    }
    if (request.method !== "POST") {
      return json({ error: "Method not allowed" }, 405, origin);
    }

    let data;
    try {
      data = await request.json();
    } catch {
      return json({ error: "Invalid JSON" }, 400, origin);
    }

    const { name, email, topic, message, company, turnstileToken } = data || {};

    // Honeypot — a bot filled the hidden "company" field. Pretend success.
    if (company && String(company).trim() !== "") {
      return json({ ok: true }, 200, origin);
    }

    // Cloudflare Turnstile — only enforced when a secret is configured, so the
    // form keeps working before Turnstile is set up. The token is verified
    // server-side with Cloudflare (the frontend token alone is not trusted).
    if (env.TURNSTILE_SECRET_KEY) {
      if (!turnstileToken) {
        return json({ error: "Verification required" }, 400, origin);
      }
      try {
        const v = await fetch("https://challenges.cloudflare.com/turnstile/v0/siteverify", {
          method: "POST",
          headers: { "Content-Type": "application/x-www-form-urlencoded" },
          body: new URLSearchParams({
            secret: env.TURNSTILE_SECRET_KEY,
            response: String(turnstileToken),
            remoteip: request.headers.get("CF-Connecting-IP") || "",
          }),
        });
        const out = await v.json();
        if (!out.success) {
          return json({ error: "Verification failed" }, 403, origin);
        }
      } catch (e) {
        return json({ error: "Verification error" }, 502, origin);
      }
    }

    // Server-side validation
    if (!name || !email || !topic || !message) {
      return json({ error: "Missing required fields" }, 400, origin);
    }
    if (!/^[^@\s]+@[^@\s]+\.[^@\s]+$/.test(email)) {
      return json({ error: "Invalid email" }, 400, origin);
    }
    const msg = String(message);
    if (msg.length < 20 || msg.length > 4000) {
      return json({ error: "Message must be between 20 and 4000 characters" }, 400, origin);
    }

    // CONTACT_TO_EMAIL may be a single address or a comma-separated list, so
    // the notification can land in more than one inbox (e.g. your domain
    // address AND your Gmail). Every listed address receives the message.
    // This list is INTERNAL — it must never be shown to visitors.
    const TO = (env.CONTACT_TO_EMAIL || "contact@ruslanmv.com")
      .split(",").map((s) => s.trim()).filter(Boolean);
    const FROM = env.CONTACT_FROM_EMAIL || "Ruslan Magana Vsevolodovna <contact@ruslanmv.com>";
    // The only address ever exposed to visitors. Always the professional domain
    // address — never a personal/Gmail inbox, even if it's in CONTACT_TO_EMAIL.
    const PUBLIC_REPLY_TO = env.CONTACT_PUBLIC_EMAIL || "contact@ruslanmv.com";
    const payload = { name, email, topic, message: msg };

    // 1) Notification to Ruslan — this one must succeed.
    const notif = buildNotificationEmail(payload);
    const r1 = await sendEmail(env, {
      from: FROM,
      to: TO,
      reply_to: email,
      subject: notif.subject,
      html: notif.html,
      text: notif.text,
    });
    if (!r1.ok) {
      return json({ error: "Email send failed", detail: r1.detail }, 502, origin);
    }

    // 2) Auto-confirmation to the sender — best effort; never fail the request
    //    just because the confirmation didn't go out.
    try {
      const conf = buildConfirmationEmail(payload);
      await sendEmail(env, {
        from: FROM,
        to: [email],
        reply_to: PUBLIC_REPLY_TO, // never the internal TO list (no Gmail leak)
        subject: conf.subject,
        html: conf.html,
        text: conf.text,
      });
    } catch (_) {
      /* swallow — notification already delivered */
    }

    return json({ ok: true }, 200, origin);
  },
};
