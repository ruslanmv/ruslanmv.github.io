/**
 * Cloudflare Worker — Contact form endpoint for ruslanmv.com
 *
 * Flow:
 *   /contact page  →  POST JSON  →  THIS Worker  →  Resend API  →  email
 *
 * The Worker sends TWO emails on a valid submission:
 *   1) NOTIFICATION  → to Ruslan (contact@ruslanmv.com)
 *   2) CONFIRMATION  → an automatic, professional acknowledgement to the sender
 *
 * The Resend API key lives ONLY here as a Worker secret (env.RESEND_API_KEY).
 * It is NEVER exposed to the browser or committed to the repo.
 *
 * Domain ruslanmv.com is verified in Resend (DKIM + SPF), so we send from
 * the real domain. See SETUP-GUIDE.md to deploy.
 *
 * FUTURE: the auto-confirmation body is generated by buildConfirmationEmail().
 * To swap in an AI-written reply later, make that function call your AI model
 * (e.g. another Worker / Anthropic API) and return { subject, html, text }.
 * Nothing else needs to change.
 */

const ALLOWED_ORIGINS = [
  "https://ruslanmv.com",
  "https://www.ruslanmv.com",
  "http://localhost:4000",
  "http://127.0.0.1:4000",
];

const CORS_BASE = {
  "Access-Control-Allow-Methods": "POST, OPTIONS",
  "Access-Control-Allow-Headers": "Content-Type",
};

function corsHeaders(origin) {
  const allow = ALLOWED_ORIGINS.includes(origin) ? origin : ALLOWED_ORIGINS[0];
  return { ...CORS_BASE, "Access-Control-Allow-Origin": allow };
}

function json(body, status, origin) {
  return new Response(JSON.stringify(body), {
    status,
    headers: { "Content-Type": "application/json", ...corsHeaders(origin) },
  });
}

// Escape user input before putting it into HTML.
const esc = (s) =>
  String(s).replace(/[&<>"']/g, (c) =>
    ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c]));

// Send one email through Resend. Returns the parsed response.
async function sendEmail(env, payload) {
  const r = await fetch("https://api.resend.com/emails", {
    method: "POST",
    headers: {
      "Authorization": `Bearer ${env.RESEND_API_KEY}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify(payload),
  });
  const detail = await r.text();
  return { ok: r.ok, status: r.status, detail };
}

